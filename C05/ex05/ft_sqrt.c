int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb <= 0)
		return (0);
	while (i * i < nb)
	{
		i++;
		if (i > nb / i)
			return (0);
	}
	if (i * i == nb)
		return (i);
	return (0);
}
